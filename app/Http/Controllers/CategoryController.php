    {
        $this->validate($request, [
            'title' => 'required',
        ]);

        $data = Category::create([
            'title' => $request->title,
            'slug' => Str::slug($request->title,'-'),
        ]);

        return response()->json([
            'message' => 'success',
            'code' => 201,
            'data' =>$data,
        ], 201);
    }

